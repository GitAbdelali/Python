# Similar to while loop, completely interchangeable

# Comes down to preference. For loop is much more 
# condensable compared to the while loop
# For Loop is primarly used to iterate (Go) through a list

# Some examples:

exampleList = [4,5,6,7,8,9,10]

# x can also be eachNumber
# assigning a variable in the list above 

# Iterater through a list
for x in exampleList: 
  print(x)
  print('Continue Program')

# For Loop as a counter
# built in function range will output all the numbers between x,xx without listing xx
for x in range(1,11):
  print(x)

  